package koreait.day2;

public class DoubleData {
public static void main(String[] args) {
	//실수 데이터 형식:Wrapper 클래스는 Float, Double
	
	System.out.println(":::4에 저장하는 Float 정수 확인:::");
	System.out.println("Float 정수의 크기 :" + Float.BYTES);
	System.out.println("Float 정수의 최솟값 :" + Float.MIN_VALUE);
	System.out.println("Float 정수의 최댓값 :" + Float.MAX_VALUE);
	
	System.out.println(":::8에 저장하는 Double 정수 확인:::");
	System.out.println("Double 정수의 크기 :" + Double.BYTES);
	System.out.println("Double 정수의 최솟값 :" + Double.MIN_VALUE);
	System.out.println("Double 정수의 최댓값 :" + Double.MAX_VALUE);
	
}
}
